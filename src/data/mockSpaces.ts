// Helper function to get spaces by category
export const getSpacesByCategory = (category: string): StudySpace[] => {
  switch (category) {
    case '24-7':
      return studySpaces.filter(space => space.is24Hours)
    case 'budget':
      return studySpaces.filter(space => space.priceRange === 'free' || space.priceRange === 'budget')
    case 'silent':
      return studySpaces.filter(space => space.noiseLevel === 'silent')
    case 'libraries':
      return studySpaces.filter(space => space.type === 'library')
    case 'cafes':
      return studySpaces.filter(space => space.type === 'cafe')
    case 'coworking':
      return studySpaces.filter(space => space.type === 'coworking')
    case 'premium':
      return studySpaces.filter(space => space.priceRange === 'premium' || space.priceRange === 'moderate')
    case 'near-metro':
      return studySpaces.filter(space => space.distance <= 2)
    default:
      return studySpaces
  }
}

// Get featured spaces
export const getFeaturedSpaces = (): StudySpace[] => {
  return studySpaces.filter(space => space.featured)
}

// Get spaces by city
export const getSpacesByCity = (city: string): StudySpace[] => {
  return studySpaces.filter(space => space.city.toLowerCase() === city.toLowerCase())
}

// Get spaces by area
export const getSpacesByArea = (area: string): StudySpace[] => {
  return studySpaces.filter(space => space.area.toLowerCase().includes(area.toLowerCase()))
}